qqfa=function(f,a=0,b=1,c=0,d=1,patience=6,epsz=1e-12,wanna_print=false)
  return qfa(x->qfa(y->f(x,y),c,d,patience,epsz,wanna_print),
	     a,b,patience,epsz,wanna_print) 
end

qqqfa=function(f,a=0,b=1,c=0,d=1,e=0,ff=1,patience=5,epsz=1e-12,wanna_print=false)
  return qfa(x->qfa(y->qfa(z->f(x,y,z),e,ff,patience,epsz,wanna_print),
	            c,d,patience,epsz,wanna_print),
	     a,b,patience,epsz,wanna_print) 
end

